// Store original English text
const englishTexts = {};

function saveEnglishTexts() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (!englishTexts[key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                englishTexts[key] = el.getAttribute('placeholder') || '';
            } else {
                englishTexts[key] = el.innerHTML;
            }
        }
    });
}

function setLanguage(lang) {
    const isInput = (el) => el.tagName === 'INPUT' || el.tagName === 'TEXTAREA';

    if (lang === 'ko') {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations.ko[key]) {
                if (isInput(el)) {
                    el.setAttribute('placeholder', translations.ko[key]);
                } else {
                    el.innerHTML = translations.ko[key];
                }
            }
        });
    } else {
        // Restore English
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (englishTexts[key] !== undefined) {
                if (isInput(el)) {
                    el.setAttribute('placeholder', englishTexts[key]);
                } else {
                    el.innerHTML = englishTexts[key];
                }
            }
        });
    }

    // Update toggle button state
    const toggle = document.querySelector('.lang-toggle');
    if (toggle) {
        toggle.querySelectorAll('.lang-option').forEach(opt => {
            opt.classList.toggle('active', opt.dataset.lang === lang);
        });
    }

    // Update html lang attribute
    document.documentElement.lang = lang === 'ko' ? 'ko' : 'en';

    // Save preference
    localStorage.setItem('slimlife-lang', lang);
}

function initI18n() {
    // Save original English text first
    saveEnglishTexts();

    // Set up toggle button click handler
    const toggle = document.querySelector('.lang-toggle');
    if (toggle) {
        toggle.addEventListener('click', (e) => {
            const option = e.target.closest('.lang-option');
            if (!option) return;
            const lang = option.dataset.lang;
            setLanguage(lang);
        });
    }

    // Apply saved language preference
    const saved = localStorage.getItem('slimlife-lang');
    if (saved && saved === 'ko') {
        setLanguage('ko');
    }
}

// Initialize when DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initI18n);
} else {
    initI18n();
}
